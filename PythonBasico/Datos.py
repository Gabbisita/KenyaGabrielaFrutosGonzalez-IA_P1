# --> Tipo de dato: String (Cadenas de texto) <--

# Los strings son como las letras de las canciones, se usan para guardar texto.
# Puedes usar comillas dobles o simples, siempre que empieces y termines con la misma.
titulo_cancion = "God's Menu"
print("Título:", titulo_cancion)

grupo = 'Stray Kids'
print("Grupo:", grupo)

# Si se mezclan las comillas, Python se confunde y da un error de sintaxis.

# --> Tipos de datos numéricos: Integer y Float <--

# Los 'integers' (int) son números enteros, sin decimales.
# Perfectos para contar los miembros de un grupo o los premios ganados.
miembros_bts = 7
print("Número de miembros en BTS:", miembros_bts)

# También pueden ser negativos, como la temperatura en un MV grabado en invierno.
temperatura_invierno = -5
print("Temperatura en el set:", temperatura_invierno)

# Los 'floats' son números con decimales.
# Ideales para valores precisos, como la duración de una canción en minutos.
duracion_dynamite = 3.19
print("Duración de 'Dynamite':", duracion_dynamite, "minutos")

# Los floats también pueden ser negativos.
variacion_stock_empresa = -1.25
print("Variación en la bolsa:", variacion_stock_empresa)


# --> Tipo de dato: Boolean (Booleano) <--

# Los booleanos solo pueden tener dos valores: True (verdadero) o False (falso).
lightstick_encendido = True
print("¿El lightstick está encendido?", lightstick_encendido)

# También sirven para verificar si una condición se cumple.
es_lider = False # Felix no es el líder de Stray Kids.
print("¿Felix es el líder?", es_lider)