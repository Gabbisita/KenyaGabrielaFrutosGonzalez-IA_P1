# --> La función len(): Contando los miembros de un grupo <--

# La función len() es súper versátil. Si le paso un texto, me dice cuántos caracteres tiene.
# Si le paso una lista o una tupla, me dice cuántos elementos contiene.

# Aquí, la usamos para contar los miembros en el line-up de TWICE.
lineup_twice = ("Nayeon", "Jeongyeon", "Momo", "Sana", "Jihyo", "Mina", "Dahyun", "Chaeyoung", "Tzuyu")
numero_de_miembros = len(lineup_twice)

print(f"El grupo TWICE tiene {numero_de_miembros} miembros.")
